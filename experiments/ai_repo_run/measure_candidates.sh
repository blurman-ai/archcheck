#!/usr/bin/env bash
# Замер кандидатов: blobless-клон → коммиты после мая 2025 → при >=MINCOM замер AI-плотности.
# Эфемерно: клон в scratch, удаляется сразу. Вывод TSV в stdout: repo  total_postmay  ai  conc
#   $1 LIST   — файл "owner/repo" по строке
#   $2 MINCOM — порог коммитов после мая (по умолч. 300)
#   $3 PAR    — параллелизм (по умолч. 3; P=16 даёт rate-limit → 66% CLONEFAIL, #066)
#   $4 RETRIES — попыток клона на репо (по умолч. 3 = ретрай ≥2, #066)
set -u
LIST="$1"; MINCOM="${2:-300}"; PAR="${3:-3}"; RETRIES="${4:-3}"
SCRATCH=/home/localadm/oss/_scratch_measure
MARK='noreply@anthropic\.com|copilot|devin-ai|cursoragent|cursor\[bot\]|google-labs-jules|Generated with Claude Code|Co-Authored-By: Claude|Generated by Devin|🤖|swe-agent'
mkdir -p "$SCRATCH"
export SCRATCH MARK MINCOM RETRIES
measure_one(){
  # .$$ — устранение коллизии имён: owner/a_b и owner_a/b оба дают owner_a_b при P>1
  repo="$1"; d="$SCRATCH/$(echo "$repo"|tr / _).$$"
  rm -rf "$d"
  # 300с (было 180): крупные established-репы с длинной историей = самый дрейф, их нельзя терять по таймауту.
  # Ретрай с нарастающим пейсингом: разовый клон ненадёжен под нагрузкой/rate-limit (#066).
  ok=0
  for try in $(seq 1 "$RETRIES"); do
    if timeout 300 git clone --filter=blob:none --quiet "https://github.com/$repo.git" "$d" 2>/dev/null; then ok=1; break; fi
    rm -rf "$d"; sleep $((try*5))
  done
  if [ "$ok" -eq 0 ]; then
    printf '%s\tCLONEFAIL\t\t\n' "$repo"; return
  fi
  total=$(git -C "$d" log --since=2025-05-01 --pretty=%H 2>/dev/null | wc -l)
  if [ "$total" -lt "$MINCOM" ]; then printf '%s\t%s\tskip\t\n' "$repo" "$total"; rm -rf "$d"; return; fi
  ai=$({ git -C "$d" log --since=2025-05-01 -i -E --author="$MARK"    --pretty=%H 2>/dev/null
         git -C "$d" log --since=2025-05-01 -i -E --committer="$MARK" --pretty=%H 2>/dev/null
         git -C "$d" log --since=2025-05-01 -i -E --grep="$MARK"      --pretty=%H 2>/dev/null; } | sort -u | wc -l)
  conc=$(( ai*100/(total>0?total:1) ))
  printf '%s\t%s\t%s\t%s\n' "$repo" "$total" "$ai" "$conc"
  rm -rf "$d"
}
export -f measure_one
grep -v '^[[:space:]]*$' "$LIST" | xargs -P "$PAR" -I{} bash -c 'measure_one "$@"' _ {}
